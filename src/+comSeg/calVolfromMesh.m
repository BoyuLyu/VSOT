function outVolume = calVolfromMesh(mF2, Vnieuw)
% Vnieuw = Vnieuw -[min(Vnieuw, 1)];

vv = 1/6*(-Vnieuw(mF2(:,3), 1).*Vnieuw(mF2(:,2), 2).*Vnieuw(mF2(:,1), 3) + ...
    Vnieuw(mF2(:,2), 1).*Vnieuw(mF2(:,3), 2).*Vnieuw(mF2(:,1), 3) +...
    Vnieuw(mF2(:,3), 1).*Vnieuw(mF2(:,1), 2).*Vnieuw(mF2(:,2), 3) -...
    Vnieuw(mF2(:,1), 1).*Vnieuw(mF2(:,3), 2).*Vnieuw(mF2(:,2), 3) -...
    Vnieuw(mF2(:,2), 1).*Vnieuw(mF2(:,1), 2).*Vnieuw(mF2(:,3), 3) +...
    Vnieuw(mF2(:,1), 1).*Vnieuw(mF2(:,2), 2).*Vnieuw(mF2(:,3), 3));
outVolume = sum(vv);
end